package com.chari.assessment.models;

public class NewsList {


    private int NewsId;
    private String NewsHeadlines;
    private String NewsSublines;


    public int getNewsId() {
        return NewsId;
    }

    public void setNewsId(int newsId) {
        NewsId = newsId;
    }

    public String getNewsHeadlines() {
        return NewsHeadlines;
    }

    public void setNewsHeadlines(String newsHeadlines) {
        NewsHeadlines = newsHeadlines;
    }

    public String getNewsSublines() {
        return NewsSublines;
    }

    public void setNewsSublines(String newsSublines) {
        NewsSublines = newsSublines;
    }


}
